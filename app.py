_CLEARANCE_STATUSES = ["Required Clearance", "Info", "Info*", "N/A"]


def _normalize_status(raw: str) -> str:
    """Map free-text status cells from an uploaded doc onto the four
    canonical values the data editor expects. Falls back to 'Required
    Clearance' if nothing matches — the adopter can correct in the editor."""
    s = (raw or "").strip().lower()
    if not s:
        return "Required Clearance"
    if s in ("info*", "info notice", "info-notice"):
        return "Info*"
    if s.startswith("info"):
        return "Info"
    if s.startswith("n/a") or s.startswith("na") or s.startswith("not"):
        return "N/A"
    if "req" in s or s in ("r", "clear", "clearance"):
        return "Required Clearance"
    return "Required Clearance"


def parse_clearance_chain_docx(raw_bytes: bytes) -> list[dict]:
    """Extract a clearance chain from the first table in a Word doc. Returns
    a list of {code, status} dicts ready for the data editor. Raises
    RuntimeError with a friendly message if the doc has no usable table."""
    try:
        import docx  # python-docx
    except ImportError as e:
        raise RuntimeError(
            "Word-doc parsing needs the python-docx package. "
            "Run `pip install -r requirements.txt` and restart this app."
        ) from e

    from io import BytesIO
    doc = docx.Document(BytesIO(raw_bytes))
    if not doc.tables:
        raise RuntimeError(
            "Couldn't find any tables in that Word document. The clearance "
            "chain needs to be a two-column table (office name / default status)."
        )
    table = doc.tables[0]
    rows = []
    for i, row in enumerate(table.rows):
        cells = [c.text.strip() for c in row.cells]
        if len(cells) < 2:
            continue
        code, status_raw = cells[0], cells[1]
        # Skip header rows heuristically: status column says 'status', or
        # the first cell looks like a column header.
        if i == 0 and (
            status_raw.lower() in ("status", "default status", "clearance status")
            or code.lower() in ("office", "office / code", "code", "bureau")
        ):
            continue
        if not code:
            continue
        rows.append({"code": code, "status": _normalize_status(status_raw)})
    if not rows:
        raise RuntimeError(
            "The first table in your document had no usable rows. The toolkit "
            "looks for a two-column layout: office name in column 1, default "
            "status in column 2."
        )
    return rows


