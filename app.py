            # Map setup-tool rows ({"code", "status"}) to the publishing
            # tool's 3-tuple shape [bureau, name, status]. We leave `name`
            # blank — adopters fill in clearer names per campaign.
            row_lines = []
            for row in edited:
                code = (row.get("code") or "").strip()
                status = (row.get("status") or "Required Clearance").strip()
                if not code:
                    continue
                row_lines.append(f"  [{js_str(code)}, \"\", {js_str(status)}],")
            array_body = "\n".join(row_lines)
            clearance_js = (
                "\nconst DEFAULT_CLEARANCES = [\n"
                f"{array_body}\n"
                "];\n"
            )
            page, n = re.subn(
                r"(/\* CLEARANCE_DEFAULTS \*/)(.*?)(/\* /CLEARANCE_DEFAULTS \*/)",
                lambda m: f"{m.group(1)}{clearance_js}{m.group(3)}",
                page,
                count=1,
                flags=re.DOTALL,
            )
            n_changes += n

            html_path.write_text(page, encoding="utf-8")
            if n_changes == 0:
                st.warning(
                    "Couldn't find the spots in the publishing tool's file to update. "
                    "The upstream HTML may have changed — you can also edit "
                    "`records-stage.html` directly."
                )
            else:
                st.success(f"Applied {n_changes} change(s) to the publishing tool.")


