def reimport(name: str):
    sys.modules.pop(name, None)
    return importlib.import_module(name)


def _read(name: str) -> str:
    return (ROOT / name).read_text(encoding="utf-8")


def parser_implemented() -> bool:
    # The shipped stub carries a unique sentinel; a generic/sample/custom
    # parser does not. This avoids false-flagging a real parser that happens
    # to contain the substring "raise NotImplementedError".
    return setup_state.PARSER_STUB_SENTINEL not in _read("parser.py")


def scorer_implemented() -> bool:
    # No scorer stub ships by default (scorer.py imports axis_scorer), so the
    # only way it's "unimplemented" is an adopter explicitly stubbing it.
    return "raise NotImplementedError" not in _read("scorer.py")


def taxonomy_opted_in() -> bool:
    src = _read("taxonomy.py")
    # Adopter has either set DEFAULT_TAXONOMY_REPO or rewritten load_taxonomy.
    if 'DEFAULT_TAXONOMY_REPO = ""' not in src:
        return True
    return "raise NotImplementedError" not in src


def _write_seam_file(path: Path, content: str) -> str | None:
    """Write a seam file (parser.py / scorer.py), backing up any existing
    *different* content to ``<name>.bak`` first. Returns the backup path (str)
    if one was made, else None. This is what makes the one-click setup buttons
    non-destructive — a hand-edited file is never silently overwritten."""
    if path.exists():
        existing = path.read_text(encoding="utf-8")
        if existing == content:
            return None
        backup = path.with_name(path.name + ".bak")
        backup.write_text(existing, encoding="utf-8")
        path.write_text(content, encoding="utf-8")
        return str(backup)
    path.write_text(content, encoding="utf-8")
    return None


class _ListLogHandler(logging.Handler):
    def __init__(self) -> None:
        super().__init__(level=logging.WARNING)
        self.messages: list[str] = []

    def emit(self, record: logging.LogRecord) -> None:
        self.messages.append(record.getMessage())


@contextmanager
def _capture_warnings():
    """Collect WARNING+ log records emitted by the toolkit modules during a
    parse/build, so they can be surfaced in the browser instead of vanishing
    into the terminal the non-technical user never sees."""
    handler = _ListLogHandler()
    root = logging.getLogger()
    prev_level = root.level
    root.addHandler(handler)
    if prev_level == logging.NOTSET or prev_level > logging.WARNING:
        root.setLevel(logging.WARNING)
    try:
        yield handler
    finally:
        root.removeHandler(handler)
        root.setLevel(prev_level)


