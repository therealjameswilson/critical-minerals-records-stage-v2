def sniff_sample_record(source_root_str: str, array_key: str | None = None) -> dict | None:
    """Peek at the first record in the user's archive so we can pre-fill
    the field-mapping dropdowns. Returns None on any error — the caller
    falls back to a less helpful manual-entry mode."""
    try:
        path = Path(source_root_str)
        files = [path] if path.is_file() else sorted(path.glob("*.json"))
        if not files:
            return None
        data = json.loads(files[0].read_text(encoding="utf-8"))
        if array_key and isinstance(data, dict):
            data = data.get(array_key, [])
        if isinstance(data, list) and data and isinstance(data[0], dict):
            return data[0]
    except Exception:
        return None
    return None


