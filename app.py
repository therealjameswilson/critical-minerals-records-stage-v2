def js_str(value: str) -> str:
    """Escape a Python str into a JS double-quoted string literal.

    Used by the publishing-tool sync to inject adopter-entered values into the
    DEFAULT_CLEARANCES / DEFAULT_DRAFTER regions of records-stage.html. Escapes
    the obvious special characters and ``</`` so a stray ``</script>`` in the
    value can't close the surrounding ``<script>`` tag.

    Module-level so the pytest suite can import and test it directly.
    """
    escaped = (
        value.replace("\\", "\\\\")
             .replace('"', '\\"')
             .replace("\n", "\\n")
             .replace("\r", "\\r")
             .replace("\t", "\\t")
             .replace("</", "<\\/")
    )
    return f'"{escaped}"'


